package com.example.youcoolmusic2.Obg;

public class PlayListItem {

    public String title;
    public int id;

    public PlayListItem(String title, int id) {
        this.title = title;
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
